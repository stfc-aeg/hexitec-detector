#ifndef DATATYPES_H_
#define DATATYPES_H_

/*
 * 32bit - 64bit compatible fixed size data types
 */

// Type                 32 bit                  64 bit
// -------------------------------------
// long                 32                              64
// ptr                  32                              64
typedef unsigned char u8;
typedef unsigned short u16;
typedef unsigned int u32;
typedef unsigned long long u64;

typedef char s8;
typedef short s16;
typedef int s32;
typedef long long s64;

#endif /*DATATYPES_H_*/
